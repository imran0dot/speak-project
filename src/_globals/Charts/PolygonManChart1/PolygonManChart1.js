            for (let i = 0; i < preselectedData.length; i++) {
              front.selectAll("[data-code="+preselectedData[i].code+"]")
                    .style("fill", preselectedData[i].severity)
                    .attr("data-severity", preselectedData[i].severity)
                    .classed("selectedEl", true)

              back.selectAll("[data-code="+preselectedData[i].code+"]")
                    .style("fill", preselectedData[i].severity)
                    .attr("data-severity", preselectedData[i].severity)
                    .classed("selectedEl", true)
            }
